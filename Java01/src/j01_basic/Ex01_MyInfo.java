package j01_basic;

public class Ex01_MyInfo {

	public static void main(String[] args) {
		
		// 이름, 나이, 전직 또는 전공분야, 수강이유, 희망취업분야(포부)
		
		String name = "최수빈";
		int age = 26;
		String job = "인쇄회사 사무";
		String reason = "미래에도 할 수 있는 일을 배우고 싶어서.";
		String hope = "백엔드";
		
		//----------------------------------------------------
				
		System.out.println("이름 : " + name);
		System.out.println("나이 : " + age);
		System.out.println("전직 : " + job);
		System.out.println("수강이유 : " + reason);
		System.out.println("희망취업 : " + hope);
		
		
	}	// main

}	// class
